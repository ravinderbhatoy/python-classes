# Operators
# 1. Arithmetic

# +, -, / *, 5, //,  **(exponentiation)

# 2. Assignment

a = 10
a += 10  # a = a + 10
print(a // 3)
# a //= 10

print(a)

# 3. Logical

# and, or, not

# and -> All true then true
# or -> If one is true then true
# not -> toggle (true -> false) (false -> True)

print(not (True or False or False))

# 4. Conditional

# >, < , >= , <= , == , !=
a = 30
b = 20

print(a != b)

# 5. Relational
# in
# not in
# is not
a = 10
b = a

print(a is not b)

nums = [1, 2, 3, 4]  # list
print(1 not in nums)
